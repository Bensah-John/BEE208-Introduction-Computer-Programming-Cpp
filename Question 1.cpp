#include <iostream>
using namespace std;


int main() {
	 float x, y, Sum, Diff, Pro, Quo;
	 
	 cout<< "enter x: ";
	 cin >>x;
	 
	 cout<<"enter y: ";
	 cin>> y;
	 
	 cout<< "Sum of numbers: ";
	 Sum= x+y;
	 cout<<Sum<<endl;
	 
	 cout<< "Difference of numbers: ";
	 Diff= x - y;
	 cout<<Diff<<endl;
	 
	 cout<< "Product of numbers: ";
	 Pro = x*y;
	 cout<<Pro<<endl;
	 
	 cout<<"Quotient of numbers: ";
	 Quo = x/y;
	 cout<< Quo;
	 
	return 0;
}
